export function transformEmoji(message) {
  if(message){
    message = message.replace(/[\n]/ig, '<br/>').replace(/[\s]/ig, '&nbsp;');
    const reg = new RegExp(regStr, 'g');
    message = message.replace(reg, function (r) {
      if (faceMap[r]) {
        return `<img class="emojiMsg" src="./static/img/wechatEmoji/${faceMap[r]}.png" oncontextmenu="return false"/>`;
      } else {
        return message
      }
    });
    const linkReg = /(http:\/\/|https:\/\/)((\w|=|\?|\.|\/|&|#|\/|%|-|:)+)/g;
    return message.replace(linkReg, '<a href="$1$2" class="custom-link" target="_blank">$1$2</a>');
  }
}


